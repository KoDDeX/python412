// Концепция листнеров
// 1. Событие загрузки документа
// 2. Группа событий связанных с формами
// 3. События связанные с кликами
// 4. События связанные с наведением мыши

// Единый список событий с детальными пояснениями:

// Загрузка документа:
// DOMContentLoaded - DOM полностью загружен и разобран, но внешние ресурсы (изображения, стили) могут быть еще не загружены
// load - страница и все ресурсы полностью загружены


// События формы:
// submit - срабатывает при отправке формы
// reset - срабатывает при сбросе формы к начальным значениям
// change - срабатывает после завершения изменения значения элемента (для input, select, textarea)
// input - срабатывает сразу при изменении значения (для text, password, textarea)
// focus - элемент получает фокус
// blur - элемент теряет фокус

// События мыши:
// click - одиночный клик левой кнопкой мыши
// dblclick - двойной клик левой кнопкой мыши
// mousedown - нажатие кнопки мыши
// mouseup - отпускание кнопки мыши
// mousemove - движение мыши
// mouseover - курсор наводится на элемент
// mouseout - курсор уходит с элемента
// mouseenter - курсор входит в элемент (не срабатывает на дочерних элементах)
// mouseleave - курсор покидает элемент (не срабатывает на дочерних элементах)
// contextmenu - вызов контекстного меню (правый клик)

// События клавиатуры:
// keydown - клавиша нажата (срабатывает для всех клавиш)
// keyup - клавиша отпущена
// keypress - нажата клавиша, генерирующая символ (устаревшее, не срабатывает для функциональных клавиш)

// События Drag & Drop:
// dragstart - начало перетаскивания
// drag - элемент перетаскивается
// dragenter - перетаскиваемый элемент входит в допустимую цель
// dragover - перетаскиваемый элемент находится над допустимой целью
// dragleave - перетаскиваемый элемент покидает допустимую цель
// drop - перетаскиваемый элемент отпущен в допустимую цель
// dragend - завершение операции перетаскивания

// События прокрутки и изменения размера:
// scroll - происходит прокрутка элемента или страницы
// resize - изменяется размер окна браузера

// События фокуса:
// focus - элемент получает фокус
// blur - элемент теряет фокус
// focusin - элемент или его потомок получает фокус (в отличие от focus, всплывает)
// focusout - элемент или его потомок теряет фокус (в отличие от blur, всплывает)

// Медиа события:
// play - воспроизведение начато или возобновлено после паузы
// pause - воспроизведение приостановлено
// ended - воспроизведение завершено
// volumechange - изменен уровень громкости или состояние mute
// timeupdate - изменилось текущее время воспроизведения

// События анимации и переходов:
// animationstart - начало CSS-анимации
// animationend - завершение CSS-анимации
// animationiteration - завершение итерации CSS-анимации
// transitionstart - начало CSS-перехода
// transitionend - завершение CSS-перехода

// События сети:
// online - браузер получил доступ к сети
// offline - браузер потерял доступ к сети

// Прочие события:
// copy - содержимое копируется в буфер обмена
// cut - содержимое вырезается в буфер обмена
// paste - содержимое вставляется из буфера обмена
// fullscreenchange - вход или выход из полноэкранного режима
// visibilitychange - изменение видимости страницы (когда пользователь переключается на другую вкладку)

// 1. Ищем div.red
let divRed = document.querySelector('.red');

// 2. Добавляем слушателя события click mouseover mouseout
divRed.addEventListener('mouseout', function () {
    // 3. Там может быть 2 класса. red и blue. Нам надо один убрать а другой добавить.
    if(divRed.classList.contains('red')) {
        divRed.classList.remove('red');
        divRed.classList.add('blue');
    }
    else {
        divRed.classList.remove('blue');
        divRed.classList.add('red');
    }

    // Альтернарнативный вариант
    // divRed.classList.toggle('blue');
})

// Функция которая переключает заливку body с красного на синий и обратно
function changeBodyColor() {
    // Определяем выполнеие кода по интервалу

    setInterval(() => {
        document.body.classList.toggle('red')
    }, 1000);
}

